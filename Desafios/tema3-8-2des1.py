import string

# Funciones útiles del módulo string:

# Todas las letras (mayúsculas y minúsculas)
letras = string.ascii_letters

# Todos los dígitos (0-9)
digitos = string.digits

# Todos los símbolos de puntuación
simbolos = string.punctuation

print(f"Letras: {letras}")
print(f"Dígitos: {digitos}")
print(f"Símbolos: {simbolos}")